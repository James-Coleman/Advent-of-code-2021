enum Error: Swift.Error {
    case unexpectedCharacter(Character)
}

enum ChunkResult {
    case complete
    case incomplete
    case corrupted(expected: Character?, found: Character)
}

/**
 - returns: If a chunk is valid or not
 */
func chunkChecker(_ input: String) throws -> ChunkResult {
    var parenCount = 0
    var squareCount = 0
    var curlyCount = 0
    var angleCount = 0
    
    var openers = [Character]()
    
    let matchingPairs: [Character?: Character] = ["(": ")",
                                                  "[": "]",
                                                  "{": "}",
                                                  "<": ">"]
    
    for character in input {
        switch character {
            case "(":
                parenCount += 1
                openers.append("(")
            case "[":
                squareCount += 1
                openers.append("[")
            case "{":
                curlyCount += 1
                openers.append("{")
            case "<":
                angleCount += 1
                openers.append("<")
            case ")":
                parenCount -= 1
                guard let lastOpener = openers.last, lastOpener == "(" else { return .corrupted(expected: matchingPairs[openers.last], found: character) }
                openers.popLast()
            case "]":
                squareCount -= 1
                guard let lastOpener = openers.last, lastOpener == "[" else { return .corrupted(expected: matchingPairs[openers.last], found: character) }
                openers.popLast()
            case "}":
                curlyCount -= 1
                guard let lastOpener = openers.last, lastOpener == "{" else { return .corrupted(expected: matchingPairs[openers.last], found: character) }
                openers.popLast()
            case ">":
                angleCount -= 1
                guard let lastOpener = openers.last, lastOpener == "<" else { return .corrupted(expected: matchingPairs[openers.last], found: character) }
                openers.popLast()
            default:
                throw Error.unexpectedCharacter(character)
        }
        
        guard parenCount >= 0, squareCount >= 0, curlyCount >= 0, angleCount >= 0 else { return .corrupted(expected: "?", found: "?") }
    }
    
    if parenCount == 0 && squareCount == 0 && curlyCount == 0 && angleCount == 0 {
        return .complete
    } else {
        return .incomplete
    }
}

do {
    try chunkChecker("()")
    try chunkChecker("[]")
    try chunkChecker("([])")
    try chunkChecker("{()()()}")
    try chunkChecker("<([{}])>")
    try chunkChecker("[<>({}){}[([])<>]]")
    try chunkChecker("(((((((((())))))))))")
    
    try chunkChecker("(]")
    try chunkChecker("{()()()>")
    try chunkChecker("(((()))}")
    try chunkChecker("<([]){()}[{}])")
} catch {
    error
}

let exampleInput = """
    [({(<(())[]>[[{[]{<()<>>
    [(()[<>])]({[<{<<[]>>(
    {([(<{}[<>[]}>{[]{[(<()>
    (((({<>}<{<{<>}{[]{[]{}
    [[<[([]))<([[{}[[()]]]
    [{[{({}]{}}([{[{{{}}([]
    {<[[]]>}<{[{[{[]{()[[[]
    [<(<(<(<{}))><([]([]()
    <{([([[(<>()){}]>(<<{{
    <{([{{}}[<[[[<>{}]]]>[]]
    """

do {
    for line in exampleInput.components(separatedBy: .newlines) {
        print(line, try chunkChecker(line))
    }
} catch {
    error
}

do {
    try chunkChecker("{([(<{}[<>[]}>{[]{[(<()>") // correctly says corrupted
    try chunkChecker("[[<[([]))<([[{}[[()]]]") // correctly says corrupted
    try chunkChecker("[{[{({}]{}}([{[{{{}}([]") // correctly says corrupted
    try chunkChecker("[<(<(<(<{}))><([]([]()") // correctly says corrupted
    try chunkChecker("<{([([[(<>()){}]>(<<{{") // correctly says corrupted
} catch {
    error
}

func score(for input: String) throws -> Int {
    var total = 0
    
    for line in input.components(separatedBy: .newlines) {
        let result = try chunkChecker(line)
        
        if case let .corrupted(_, found) = result {
            switch found {
                case ")": total += 3
                case "]": total += 57
                case "}": total += 1197
                case ">": total += 25137
                default: throw Error.unexpectedCharacter(found)
            }
        }
    }
    
    return total
}

do {
    try score(for: exampleInput) // 26397 (correct)
} catch {
    error
}

